package quadric.gfs;

public class GfsAdHoc {
	private String uuid;
	private int quantity;
	private int siteId; 
	
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int card) {
		this.quantity = card;
	}
	public int getSiteId() {
		return siteId;
	}
	public void setSiteId(int siteId) {
		this.siteId = siteId;
	}
}
